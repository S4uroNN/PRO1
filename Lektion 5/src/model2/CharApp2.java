package model2;

public class CharApp2 {
    public static void main(String[] args) {
        int i1;
        char ch1, ch2;
        ch1 = 'A';
        System.out.println(ch1);
        i1 = ch1;
        System.out.println(i1);
        i1 = 66;
        System.out.println(i1);
        ch2 = (char) i1;
        System.out.println(ch2);
        char ch3 = '3';
        i1 = ch3;
        System.out.println(i1);
    }
}
